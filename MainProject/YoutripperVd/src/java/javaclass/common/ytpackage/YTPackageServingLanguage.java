package javaclass.common.ytpackage;

/**
 *
 * @author nickn
 */
public class YTPackageServingLanguage {

    private String code;
    private String name;

    public YTPackageServingLanguage() {
    }

    public YTPackageServingLanguage(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

}
